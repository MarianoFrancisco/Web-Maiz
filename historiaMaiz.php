<p ALIGN="JUSTIFY">El Popol Vuh, libro sagrado de los mayas, describe la creación del hombre y la forma en que el maíz
    se convierte en símbolo mágico de nuestra cultura. Dice así:
    … Y de esta manera se llenaron de alegría, porque habían descubierto una hermosa tierra, llena de deleites,
    abundante en mazorcas amarillas y mazorcas blancas y abundante también en pataxte y cacao, y en innumerables
    zapotes, anonas, jocotes, nances, matasanos y miel. […]
    A continuación entraron en pláticas acerca de la creación y la formación de nuestra primera madre y padre. De maíz
    amarillo y de maíz blanco se hizo su carne; de masa de maíz se hicieron los brazos y las piernas del hombre.
    Únicamente masa de maíz entró en la carne de nuestros padres, los cuatro hombres que fueron creados.
    Éstos son los nombres de los primeros hombres que fueron creados y formados: el primer hombre fue Balam-Quitzé, el
    segundo Balam-Acab, el tercero Mahucutah y el cuarto Iqui-Balam.</p><br><br>
<p ALIGN="JUSTIFY">El maíz es el segundo cultivo del mundo, después del trigo, mientras que el arroz ocupa el tercer
    lugar. Tan importante ha sido este grano para nuestros pueblos que en el Popol Vuh, libro sagrado, se narra que la
    humanidad surgió de esta planta. Luego de varios intentos los dioses logran su propósito y crean al hombre, al que
    forman con maíz. Así aparecieron Balam Quitzé, Balam Acab, Manucutah e Iqui Balam. Eran bellos, fuertes,
    inteligentes, veían y sabían todo. Eran muy sabios, tanto que las deidades empezaron a molestarse: ¡Ven demasiado,
    oyen demasiado, conocen el mundo como nosotros! ¡Parecen dioses!, se puede leer en este documento que relata la
    mitología y la historia del pueblo k’iche’.
    Desde los antiguos mayas el maíz ha sido uno de los componentes básicos en nuestra dieta, ha sido sustento sagrado
    de nuestro pueblo, ya que con su producción se asentaron nuestras comunidades; se generaron ciencia, tecnología,
    arte, pero, sobre todo, se estableció una sagrada comunión con la madre naturaleza y se tejieron profundos lazos de
    familiaridad en la sociedad, refiere el historiador e investigador Luis Villar Anleu.
    De ahí el sentido de la semilla. Su grano es una semilla que, aunada con el alimento de la madre tierra, la
    luz-calor del padre sol, la humedad-vitalidad de la madre agua y el soplo-aliento del padre aire, floreció un nuevo
    fruto, explican desde el Consejo Nacional de Educación Maya (CNEM).
    Resulta curioso que este cereal no pueda crecer de forma autónoma. Sin el cuidado del ser humano no existiría. Este
    factor, se cree, fue fundamental para haber unido a ambas partes formando un dúo casi indivisible a lo largo de los
    siglos, comenta Villar Anleu.</p>
<h3 style="color: #FFD100;">Deidad dual:</h3>

<p ALIGN="JUSTIFY">En muchos de nuestros pueblos se conserva el culto al dios maíz, porque significa un principio y un
    elemento fundamental de su cosmovisión, pues este grano sigue siendo un dador de vida y un elemento de identidad. En
    Huehuetenango, por ejemplo, el maíz puede ser un dios masculino, como en el caso de Santiago Chimaltenango, donde
    recibe el nombre de padre paxil, o femenino, como en Colotenango, donde se le denomina K’txu (Nuestra madre). De
    acuerdo con el Popol Vuh, Paxil es el nombre del lugar donde se originó este cereal.</p>
<h3 style="color: #FFD100;">Origen y celebración:</h3>
<p ALIGN="JUSTIFY">Hoy no hay dudas del origen americano del maíz (Zea mays L), pero nunca fue mencionado en ningún
    tratado antiguo hasta el descubrimiento de América por Cristóbal Colón, quien lo vio por primera vez en la isla de
    Cuba en octubre de 1492.
    Se cree que este grano surgió aproximadamente entre los años 8000 y 600 a.C en Mesoamérica, debido a su peculiar
    ecosistema (invierno seco estacional en alternancia con las lluvias de verano) y sus regiones montañosas, de cuestas
    empinadas y sobre roca caliza.
    El maíz como cultivo es un sistema dinámico y continuo. Su polinización es libre y hay movimiento o flujo de semilla
    por los agricultores año con año al mantener, intercambiar y experimentar con semilla propia o de otros vecinos de
    la misma localidad o de regiones distantes.
    Una aproximación a su estudio y entendimiento ha sido seleccionar sus principales unidades (tipos o formas) que le
    caracterizan y a las que se han denominado razas, desde la más antigua (teocinte) hasta las más evolucionadas. En
    América Latina se han descrito cerca de 220.
    En el altiplano occidental de Guatemala se localiza la mayor diversidad genética de este grano, donde hay 13 razas y
    9 sub-razas. De acuerdo con la FAO, de las 14 razas reportadas para Centroamérica, nuestro país tiene 13, e
    infinidad de variedades.
    Por ser uno de los símbolos más valiosos y arraigados de la cultura y la naturaleza local y por tener la mayor
    diversidad genética, en el país se celebra el Día Nacional del Maíz cada 13 de agosto, desde que fue declarado
    Patrimonio Cultural Intangible de la Nación.</p>
<h3 style="color: #FFD100;">Gastronomía:</h3>
<p ALIGN="JUSTIFY">Los mayas preparaban este grano, hirviéndolo o remojándolo en agua con cal que luego era drenado;
    mientras se encontraba húmedo, se molía en un metate con una piedra de moler o a mano. La masa resultante se
    mezclaba luego con agua y chile para hacerlo más espeso para luego palmearlo y darle forma redonda y plana, lo que
    hoy conocemos como tortilla.
    De las tres variedades más usadas: grano amarillo, blanco o negro, la primera se prefiere en las poblaciones de
    occidente, el resto del país se inclina por el blanco. El negro se estima mucho en el altiplano occidental, donde
    sus tortillas son muy gustadas y en algunos lugares es materia para un alto sacro-ceremonial, describe Villar Anleu.
    En el libro La cocina popular guatemalteca; mitos, hechos y anécdotas, autoría del propio Villar Anleu, se cita que
    el elote asado a las brasas es una de las comidas más antiguas de Que se tenga notica, de hecho, hasta nuestros días
    siguen siendo una apreciada preparación, con naturales cambios que han adatado a multitud de gustos.
    Pero es tan versátil este grano que también puede degustarse cocido, en atol, pan o en tamalitos. Entre las comidas
    hechas a base de maíz nixtamalizado el primer nombre en la lista es el de las tortillas, las reinas de la identidad
    alimentaria de los guatemaltecos, y por ello infaltables en su dieta. Una variedad más gruesa, preferida como avío
    de viaje, se llama pixtón.</p>
<h3 style="color: #FFD100;">Algunas especialidades:</h3>
<p ALIGN="JUSTIFY"><b style="color: #FF6800;">Los boxboles de Baja Verapaz.</b> En esta región del país se acostumbra a
    comer el maíz en boxboles, que
    son preparados de distinta forma. En Purulhá, por ejemplo, una de las primeras tareas es subir a las montañas hasta
    el dominio de las frías selvas de neblina a recolectar hojas de roq’tix (pie de viejita). Ya se ha preparado la masa
    de maíz, que luego se envuelve por poquitos en las hojas recolectadas, limpias y crudas. Se van enrollando como
    tamalitos y se cuecen. Por aparte, se prepara una salsa de tomate, pepita de ayote y chile, que servirá para bañar
    los boxboles. Ojo, se comen con todo y hojas, remarca Villar Anleu.</p>
<p ALIGN="JUSTIFY"><b style="color: #FF6800;">Los bollos de Petén.</b> De la cocina petenera vale destacar los bollos,
    que forman una entidad culinaria
    definida. Son tamales no muy grandes, que se envuelven en hoja de plátano. La diferencia de sus variedades depende
    de los ingredientes añadidos: frijoles, chaya, izote, carne picada o palmito, indica el historiador.</p>
<p ALIGN="JUSTIFY"><b style="color: #FF6800;">Tamales.</b> Los tamales constituyen la culminación de las comidas hechas
    a base de maíz y son referente
    obligado de la cocina guatemalteca.</p>
